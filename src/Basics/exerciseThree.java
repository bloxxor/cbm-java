package Basics;

public class exerciseThree {

    public static void main(String[] args) {

        float numberOne = 5f;
        float numberTwo = 8f;
        float calcResult;

        calcResult = numberOne + numberTwo;
        System.out.println("Addition: " + calcResult);

        calcResult = numberOne - numberTwo;
        System.out.println("Subtraction: " + calcResult);

        calcResult = numberOne * numberTwo;
        System.out.println("Multiplication: " + calcResult);

        calcResult = numberOne / numberTwo;
        System.out.println("Division: " + calcResult);

    }

}
