package Basics;

public class CarLoop {

    public static void main(String[] args) {

        String[] cars = {
                "Ford Mustang (1969 Fastback)",
                "Chevrolet Chevelle SS (1968)",
                "Chevrolet Camaro Coupe SS (1967)",
                "Dodge Charger (1968)",
                "Ford Mustang (1967)",
        };

        for (String car : cars ) {
            System.out.println(car);
        }

    }

}
