package Basics;

public class VariableConnect {

    public static void main(String[] args) {

        // Initialize and declare variables
        String textPartOne = "Good morning!";
        String textPartTwo = "Everyone!";
        String completeSentence = textPartOne + " " + textPartTwo;

        // Connect variables and print them out
        System.out.println(completeSentence);

    }

}
