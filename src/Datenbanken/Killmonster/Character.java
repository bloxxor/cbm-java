package Datenbanken.Killmonster;

public class Character {

    String name;

    String charClass;

    int strength;
    int defense;
    int dexterity;

    public Character(String name, String charClass, int strength, int defense, int dexterity) {
        this.name = name;
        this.charClass = charClass;
        this.strength = strength;
        this.defense = defense;
        this.dexterity = dexterity;
    }

}
