package UML.GeometryDisplay;

public class GeoDot {

    private double x;
    private double y;

    public GeoDot(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getX() {
        return x;
    }

    public void setY(double y) {
        this.y = y;
    }

    public double getY() {
        return y;
    }

}
